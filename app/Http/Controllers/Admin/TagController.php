     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required'
        ]);

        Tag::create([
            'name' => $request->name,
        ]);

        alert()->success('ذخیره تگ با موفقیت انجام شد');
        return redirect()->route('admin.tags.index');
    }

